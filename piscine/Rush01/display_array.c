#include <unistd.h>

void	display_array(int array[4][4])
{
	int		i;
	int		j;
	char	a;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			a = array[i][j] + '0';
			write(1, &a, 1);
			if (j < 3)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
